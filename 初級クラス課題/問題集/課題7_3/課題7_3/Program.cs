    class Program
    {
        static void Main(string[] args)
        {
            string[] colors = { "赤", "青", "黄", "緑", "白" };

            for (int i = 0; i < colors.Length; i++)
            {
                Console.WriteLine(colors[i]);
            }

            Console.ReadLine();
        }
    }
